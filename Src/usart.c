void ConfigUart7DMA(void)
{
	Uart7DMAMemoryBaseAddress0=(uint32_t)RxBuf0;//接收数据缓冲数组（内存）
	Uart7DMAMemoryBaseAddress1=(uint32_t)RxBuf1;
	Uart7DMABufferSize=1024;//接收数据的长度
}

void UART7ConfigEnable(void)
{
	HAL_DMAEx_MultiBufferStart(&hdma_uart7_rx, (uint32_t)&UART7->DR, Uart7DMAMemoryBaseAddress0,Uart7DMAMemoryBaseAddress1, Uart7DMABufferSize);
  //将数据存入DMA双缓冲区                               外设地址       内存地址 
	__HAL_UART_ENABLE_IT(&huart7, UART_IT_IDLE);   //使能空闲中断IDLE模式
	SET_BIT(UART7->CR3, USART_CR3_DMAR);    //使能DMA接收   
	__HAL_DMA_ENABLE_IT(&hdma_uart7_rx, DMA_IT_TC);   //使能传输完成中断
	__HAL_DMA_ENABLE(&hdma_uart7_rx); 
}
